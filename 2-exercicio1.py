valor_a = int(input("Insira o valor de 'a': "))
valor_b = int(input("Insira o valor de 'b': "))

if valor_a > valor_b:
    print("O valor de 'a' é maior que o valor de 'b'.")
elif valor_a < valor_b:
    print("O valor de 'b' é maior que o valor de 'a'.")
else:
    print("Os valores de 'a' e 'b' são iguais.")